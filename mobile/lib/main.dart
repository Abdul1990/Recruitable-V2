// Handle background push notifications
@pragma('vm:entry-point')
Future<void> _firebaseMessagingBackgroundHandler(RemoteMessage message) async {
  await Firebase.initializeApp(options: DefaultFirebaseOptions.currentPlatform);
}

void main() async {
  WidgetsFlutterBinding.ensureInitialized();
  await Firebase.initializeApp(options: DefaultFirebaseOptions.currentPlatform);
  FirebaseMessaging.onBackgroundMessage(_firebaseMessagingBackgroundHandler);
  runApp(const RecruitableApp());
}

// ─── Router ───────────────────────────────────────────────────────────────────

final _router = GoRouter(
  initialLocation: '/',
  redirect: (context, state) {
    final user = AuthService.instance.currentUser;
    final isAuth = user != null;
    final isOnAuthPath = state.matchedLocation.startsWith('/auth') ||
        state.matchedLocation == '/';

    if (!isAuth && !isOnAuthPath) return '/';
    return null;
  },
  routes: [
    // Splash / landing
    GoRoute(
      path: '/',
      builder: (_, __) => const SplashScreen(),
    ),

    // LinkedIn OAuth WebView
    GoRoute(
      path: '/auth/linkedin',
      builder: (_, __) => const LinkedInAuthScreen(),
    ),

    // Onboarding
    GoRoute(
      path: '/onboarding/skills',
      builder: (_, __) => const SkillSelectionScreen(),
    ),
    GoRoute(
      path: '/onboarding/location',
      builder: (_, __) => const LocationPickerScreen(),
    ),

    // Main swipe feed
    GoRoute(
      path: '/feed',
      builder: (_, __) => const SwipeFeedScreen(),
    ),

    // Matches list
    GoRoute(
      path: '/matches',
      builder: (_, __) => const MatchesScreen(),
    ),

    // Chat
    GoRoute(
      path: '/matches/:matchId/chat',
      builder: (context, state) {
        final matchId = state.pathParameters['matchId']!;
        final match = state.extra as Match;
        return ChatScreen(match: match);
      },
    ),

    // Profile
    GoRoute(
      path: '/profile',
      builder: (_, __) => const ProfileScreen(),
    ),
  ],
);

