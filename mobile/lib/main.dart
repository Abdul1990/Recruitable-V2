class LocationPickerScreen extends StatefulWidget {
  const LocationPickerScreen({super.key});

  @override
  State<LocationPickerScreen> createState() => _LocationPickerScreenState();
}

class _LocationPickerScreenState extends State<LocationPickerScreen> {
  String? _selected;
  bool _saving = false;

  Future<void> _confirm() async {
    if (_selected == null || _saving) return;
    setState(() => _saving = true);

    final uid = AuthService.instance.currentUser?.uid;
    if (uid == null) return;

    await FirestoreService.instance.updateLocationOnly(uid: uid, location: _selected!);
    if (mounted) context.go('/feed');
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: Colors.white,
      appBar: AppBar(
        backgroundColor: Colors.white,
        elevation: 0,
        title: const Text(
          'Where are you based?',
          style: TextStyle(fontFamily: 'Nunito', fontWeight: FontWeight.w800, fontSize: 18, color: Color(0xFF212121)),
        ),
      ),
      body: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const Padding(
            padding: EdgeInsets.fromLTRB(24, 8, 24, 20),
            child: Text(
              'We focus on APAC opportunities.\nSelect your base — we\'ll show you local roles first.',
              style: TextStyle(fontFamily: 'Nunito', fontWeight: FontWeight.w500, fontSize: 14, color: Color(0xFF8A8A9E), height: 1.5),
            ),
          ),
          Expanded(
            child: ListView.separated(
              padding: const EdgeInsets.symmetric(horizontal: 24),
              itemCount: AppConfig.apacRegions.length,
              separatorBuilder: (_, __) => const SizedBox(height: 10),
              itemBuilder: (context, i) {
                final region = AppConfig.apacRegions[i];
                final isSelected = _selected == region.code;
                return GestureDetector(
                  onTap: () => setState(() => _selected = region.code),
                  child: AnimatedContainer(
                    duration: const Duration(milliseconds: 180),
                    padding: const EdgeInsets.symmetric(horizontal: 20, vertical: 18),
                    decoration: BoxDecoration(
                      color: isSelected ? const Color(0xFFE91E63) : const Color(0xFFF4F6FB),
                      borderRadius: BorderRadius.circular(16),
                      border: Border.all(
                        color: isSelected ? const Color(0xFFE91E63) : const Color(0xFFE8ECF4),
                        width: isSelected ? 2 : 1,
                      ),
                    ),
                    child: Row(
                      children: [
                        Text(region.flag, style: const TextStyle(fontSize: 28)),
                        const SizedBox(width: 16),
                        Expanded(
                          child: Column(
                            crossAxisAlignment: CrossAxisAlignment.start,
                            children: [
                              Text(
                                region.label,
                                style: TextStyle(
                                  fontFamily: 'Nunito',
                                  fontWeight: FontWeight.w800,
                                  fontSize: 17,
                                  color: isSelected ? Colors.white : const Color(0xFF212121),
                                ),
                              ),
                              Text(
                                region.city,
                                style: TextStyle(
                                  fontFamily: 'Nunito',
                                  fontWeight: FontWeight.w500,
                                  fontSize: 12,
                                  color: isSelected ? Colors.white.withOpacity(0.8) : const Color(0xFF8A8A9E),
                                ),
                              ),
                            ],
                          ),
                        ),
                        if (isSelected)
                          const Icon(Icons.check_circle, color: Colors.white, size: 22),
                      ],
                    ),
                  ),
                );
              },
            ),
          ),

          // "I'm elsewhere" escape hatch — non-APAC users can still sign up
          Padding(
            padding: const EdgeInsets.fromLTRB(24, 12, 24, 8),
            child: GestureDetector(
              onTap: () => setState(() => _selected = 'OTHER'),
              child: AnimatedContainer(
                duration: const Duration(milliseconds: 180),
                padding: const EdgeInsets.symmetric(horizontal: 20, vertical: 14),
                decoration: BoxDecoration(
                  color: _selected == 'OTHER' ? const Color(0xFF212121) : Colors.white,
                  borderRadius: BorderRadius.circular(16),
                  border: Border.all(
                    color: _selected == 'OTHER' ? const Color(0xFF212121) : const Color(0xFFE0E0E0),
                  ),
                ),
                child: Row(
                  children: [
                    const Text('🌍', style: TextStyle(fontSize: 24)),
                    const SizedBox(width: 14),
                    Text(
                      'I\'m based elsewhere',
                      style: TextStyle(
                        fontFamily: 'Nunito',
                        fontWeight: FontWeight.w700,
                        fontSize: 15,
                        color: _selected == 'OTHER' ? Colors.white : const Color(0xFF424242),
                      ),
                    ),
                    const Spacer(),
                    if (_selected == 'OTHER')
                      const Icon(Icons.check_circle, color: Colors.white, size: 22),
                  ],
                ),
              ),
            ),
          ),

          // CTA
          SafeArea(
            child: Padding(
              padding: const EdgeInsets.fromLTRB(24, 8, 24, 24),
              child: SizedBox(
                width: double.infinity,
                height: 52,
                child: ElevatedButton(
                  onPressed: _selected != null && !_saving ? _confirm : null,
                  style: ElevatedButton.styleFrom(
                    backgroundColor: const Color(0xFFE91E63),
                    disabledBackgroundColor: const Color(0xFFE0E0E0),
                    shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(14)),
                    elevation: 0,
                  ),
                  child: _saving
                      ? const SizedBox(width: 22, height: 22, child: CircularProgressIndicator(color: Colors.white, strokeWidth: 2.5))
