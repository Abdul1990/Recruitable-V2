                          'Show me roles',
                          style: TextStyle(fontFamily: 'Nunito', fontWeight: FontWeight.w800, fontSize: 16, color: Colors.white),
                        ),
                ),
              ),
            ),
          ),
        ],
      ),
    );
  }
}

class MatchesScreen extends StatelessWidget {
  const MatchesScreen({super.key});

  @override
  Widget build(BuildContext context) {
    final uid = AuthService.instance.currentUser?.uid ?? '';
    return Scaffold(
      appBar: AppBar(title: const Text('My Matches')),
      body: StreamBuilder(
        stream: FirestoreService.instance.watchMatches(uid),
        builder: (context, snapshot) {
          final matches = snapshot.data ?? <Match>[];
          if (matches.isEmpty) {
            return const Center(
              child: Text('No matches yet.\nKeep swiping!', textAlign: TextAlign.center,
                  style: TextStyle(fontFamily: 'Nunito', fontSize: 16, color: Color(0xFF8A8A9E))),
            );
          }
          return ListView.builder(
            padding: const EdgeInsets.all(16),
            itemCount: matches.length,
            itemBuilder: (context, i) {
              final m = matches[i];
              return ListTile(
                leading: CircleAvatar(
                  backgroundColor: const Color(0xFFE91E63),
                  child: Text(m.company[0], style: const TextStyle(color: Colors.white, fontFamily: 'Nunito', fontWeight: FontWeight.w800)),
                ),
                title: Text(m.company, style: const TextStyle(fontFamily: 'Nunito', fontWeight: FontWeight.w700)),
                subtitle: Text(m.jobTitle, style: const TextStyle(fontFamily: 'Nunito', fontSize: 12)),
                trailing: Column(
                  mainAxisSize: MainAxisSize.min,
                  children: [
                    Text('${m.matchScore}%', style: const TextStyle(fontFamily: 'Nunito', fontWeight: FontWeight.w900, color: Color(0xFFE91E63))),
                    if (m.hasUnreadMessages)
                      Container(width: 8, height: 8, decoration: const BoxDecoration(color: Color(0xFFE91E63), shape: BoxShape.circle)),
                  ],
                ),
                onTap: () => context.go('/matches/${m.id}/chat', extra: m),
              );
            },
          );
        },
      ),
    );
  }
}

class ProfileScreen extends StatelessWidget {
  const ProfileScreen({super.key});

  @override
  Widget build(BuildContext context) {
    final uid = AuthService.instance.currentUser?.uid ?? '';
    return Scaffold(
      appBar: AppBar(title: const Text('My Profile')),
      body: StreamBuilder(
        stream: FirestoreService.instance.watchUser(uid),
        builder: (context, snapshot) {
          final profile = snapshot.data;
          if (profile == null) return const Center(child: CircularProgressIndicator(color: Color(0xFFE91E63)));
          return Center(child: Text('Profile: ${profile.displayName}'));
        },
      ),
    );
  }
}
